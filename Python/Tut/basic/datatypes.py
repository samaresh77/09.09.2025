# data types in Python
x = 5               # integer
y = 3.14            # float
name = "Alice"     # string
is_student = True   # boolean
fruits = ["apple", "banana", "cherry"]  # list
person = {"name": "Bob", "age": 25}      # dictionary

""" Data Types: data types are classifications that specify which type of value a variable can hold. In Python, common data types include:
1. Integer (int): Whole numbers, e.g., 5, -3, 0
2. Float (float): Decimal numbers, e.g., 3.14, -0.001
3. String (str): Sequence of characters, e.g., "Hello, World!"
4. Boolean (bool): Represents True or False values
5. List (list): Ordered collection of items, e.g., [1, 2, 3]
6. Dictionary (dict): Collection of key-value pairs, e.g., {"name": "Alice", "age": 30}."""

""" Type Conversion: Python provides built-in functions to convert between different data types. For example:
- int(): Converts a value to an integer
- float(): Converts a value to a float
- str(): Converts a value to a string
- list(): Converts a value to a list
- dict(): Converts a value to a dictionary
Example:
num_str = "10"
num_int = int(num_str)  # converts string to integer
num_float = float(num_int)  # converts integer to float
print(num_int, num_float)  # Output: 10 10.0"""