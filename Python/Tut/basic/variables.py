a = 10
b = 20

print("Sum:", a + b)

""" Variables: A variable is a named location used to store data in the memory. 
In Python, you can create a variable by simply assigning a value to a name 
using the equals sign (=). For example, in the code above, 'a' and 'b' 
are variables that store integer values. The sum of these two variables 
is then calculated and printed."""

""" Variable Naming Rules:
1. Variable names must start with a letter (a-z, A-Z) or an underscore (_).
2. The rest of the variable name can contain letters, digits (0-9), or underscores.
3. Variable names are case-sensitive (e.g., 'myVar' and 'myvar' are different).
4. Variable names cannot be the same as Python reserved keywords (e.g., 'if', 'for', 'while')."""